const submapSlots = [
  { x: 50, y: 15 },
  { x: 68, y: 20 },
  { x: 82, y: 34 },
  { x: 86, y: 52 },
  { x: 76, y: 70 },
  { x: 58, y: 82 },
  { x: 40, y: 82 },
  { x: 24, y: 70 },
  { x: 14, y: 52 },
  { x: 18, y: 34 },
  { x: 32, y: 20 },
  { x: 50, y: 90 },
];

function buildNode(parentId, item, index) {
  const slot = submapSlots[index % submapSlots.length];
  return {
    id: `${parentId}-${item.id}`,
    label: item.label,
    en: item.en,
    x: slot.x,
    y: slot.y,
    orbit: 1 + (index % 3),
    tone: item.tone ?? ['cyan', 'blue', 'amber'][index % 3],
    signals: 5 + (index % 6),
    keywords: item.keywords ?? [item.label, item.en],
    questions: item.questions ?? [`${item.label}은 SF를 어떤 방식으로 다시 분류하게 만드는가?`],
    concepts: item.concepts ?? [item.label],
  };
}

function buildSubmap(parentId, description, items, extraConnections = []) {
  const nodes = items.map((item, index) => buildNode(parentId, item, index));
  return {
    description,
    nodes,
    connections: [
      ...nodes.map(node => [parentId, node.id]),
      ...extraConnections,
    ],
  };
}

